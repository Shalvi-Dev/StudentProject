package com.beans;

import java.util.Date;

public class StudentInfo {
	private int stud_id;
	private String stud_name;
	private Date stud_DOB;
	private Date stud_DOJ;
	
	public int getStud_id() {
		return stud_id;
	}
	public void setStud_id(int stud_id) {
		this.stud_id = stud_id;
	}
	public String getStud_name() {
		return stud_name;
	}
	public void setStud_name(String stud_name) {
		this.stud_name = stud_name;
	}
	public Date getStud_DOB() {
		return stud_DOB;
	}
	public void setStud_DOB(Date stud_DOB) {
		this.stud_DOB = stud_DOB;
	}
	public Date getStud_DOJ() {
		return stud_DOJ;
	}
	public void setStud_DOJ(Date stud_DOJ) {
		this.stud_DOJ = stud_DOJ;
	}

}
